package com.bestmovies.sep6_project.restclient;

public class Key {
    private String api_key;
    private String api_bearer;

    public String getApi_key() {
        return api_key;
    }

    public void setApi_key(String api_key) {
        this.api_key = api_key;
    }

    public String getApi_bearer() {
        return api_bearer;
    }

    public void setApi_bearer(String api_bearer) {
        this.api_bearer = api_bearer;
    }
}
